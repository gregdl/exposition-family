print.distatis.table <- function(x,...){
	
	res.distatis.table <- x
	if(!inherits(res.distatis.table,"distatis.table"))stop("no convenient data")
	cat("**Results for the Tables via DISTATIS**\n\n")
	cat("The results are available for the following objects: \n\n")
	res <- array("",c(8,2),list(1:8,c("Name","Description")))
	
	res[1,] <- c("$a","Weights (alpha)")
	res[2,] <- c("$m","Masses")
	res[3,] <- c("$eigs","Eigen Values for the Tables")
	res[4,] <- c("$t","Inertia for the Tables (tau)")
	res[5,] <- c("$eigs.vector","Eigen Vectors for the Tables")
	res[6,] <- c("$fi","Factor Scores for the Tables")
	res[7,] <- c("$partial.fi","Partial Factor Scores for the Tables")
	res[8,] <- c("$ci","Contributions of the Row for the Tables")


	print(res)	
}